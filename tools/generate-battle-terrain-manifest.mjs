import { readFile, rename, stat, unlink, writeFile } from 'node:fs/promises';
import path from 'node:path';

const TERRAIN_FILENAMES = [
  'battle_terrain_base_720x1280.png',
  'battle_road_overlay.png',
  'battle_ruins_left.png',
  'battle_ruins_right.png',
  'battle_atmosphere.png',
  'battle_wall_back.png',
  'battle_wall_front.png',
];

const REPOSITORY_MARKER = 'package.json';
const ASSET_DIRECTORY = path.join('assets', 'bundles', 'battle_common');
const OUTPUT_PATH = path.join('assets', 'scripts', 'data', 'BattleTerrainAssets.generated.ts');

function displayPath(filePath) {
  return filePath.split(path.sep).join('/');
}

async function assertRepositoryRoot(repositoryRoot) {
  const markerPath = path.join(repositoryRoot, REPOSITORY_MARKER);
  const assetDirectory = path.join(repositoryRoot, ASSET_DIRECTORY);

  try {
    const [markerStats, assetStats] = await Promise.all([stat(markerPath), stat(assetDirectory)]);

    if (!markerStats.isFile() || !assetStats.isDirectory()) {
      throw new Error('repository markers have unexpected types');
    }
  } catch (error) {
    const detail = error instanceof Error ? ` (${error.message})` : '';
    throw new Error(
      `Run this script from the repository root; expected ${REPOSITORY_MARKER} and ${displayPath(ASSET_DIRECTORY)}${detail}`,
    );
  }
}

async function readMetaUuid(repositoryRoot, filename) {
  const relativeMetaPath = path.join(ASSET_DIRECTORY, `${filename}.meta`);
  const absoluteMetaPath = path.join(repositoryRoot, relativeMetaPath);
  let source;

  try {
    source = await readFile(absoluteMetaPath, 'utf8');
  } catch (error) {
    if (error && typeof error === 'object' && error.code === 'ENOENT') {
      throw new Error(`Missing metadata file: ${displayPath(relativeMetaPath)}`);
    }

    const detail = error instanceof Error ? `: ${error.message}` : '';
    throw new Error(`Unable to read ${displayPath(relativeMetaPath)}${detail}`);
  }

  let metadata;
  try {
    metadata = JSON.parse(source);
  } catch (error) {
    const detail = error instanceof Error ? `: ${error.message}` : '';
    throw new Error(`Invalid JSON in ${displayPath(relativeMetaPath)}${detail}`);
  }

  if (
    metadata === null ||
    typeof metadata !== 'object' ||
    Array.isArray(metadata) ||
    typeof metadata.uuid !== 'string' ||
    metadata.uuid.trim().length === 0
  ) {
    throw new Error(`Missing non-empty top-level uuid in ${displayPath(relativeMetaPath)}`);
  }

  return metadata.uuid.trim();
}

function renderManifest(entries) {
  const uuidMap = Object.fromEntries(entries);
  return `// Generated by tools/generate-battle-terrain-manifest.mjs.\nexport const BATTLE_TERRAIN_ASSET_UUIDS = ${JSON.stringify(uuidMap, null, 2)} as const;\n`;
}

async function writeAtomically(repositoryRoot, source) {
  const outputPath = path.join(repositoryRoot, OUTPUT_PATH);
  const temporaryPath = `${outputPath}.${process.pid}.${Date.now()}.tmp`;
  let temporaryFileExists = false;

  try {
    temporaryFileExists = true;
    await writeFile(temporaryPath, source, 'utf8');
    await rename(temporaryPath, outputPath);
    temporaryFileExists = false;
  } catch (error) {
    const detail = error instanceof Error ? `: ${error.message}` : '';
    throw new Error(`Unable to write ${displayPath(OUTPUT_PATH)} atomically${detail}`);
  } finally {
    if (temporaryFileExists) {
      await unlink(temporaryPath).catch(() => undefined);
    }
  }
}

async function main() {
  const repositoryRoot = process.cwd();
  await assertRepositoryRoot(repositoryRoot);

  const filenames = [...TERRAIN_FILENAMES].sort((left, right) => left.localeCompare(right, 'en'));
  const entries = [];
  const uuidOwners = new Map();

  for (const filename of filenames) {
    const uuid = await readMetaUuid(repositoryRoot, filename);
    const previousFilename = uuidOwners.get(uuid);

    if (previousFilename) {
      throw new Error(
        `Duplicate top-level uuid "${uuid}" in ${previousFilename}.meta and ${filename}.meta`,
      );
    }

    uuidOwners.set(uuid, filename);
    entries.push([filename, uuid]);
  }

  await writeAtomically(repositoryRoot, renderManifest(entries));
  console.log(`Generated ${displayPath(OUTPUT_PATH)} with ${entries.length} entries.`);
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`[battle-terrain-manifest] ${message}`);
  process.exitCode = 1;
});
